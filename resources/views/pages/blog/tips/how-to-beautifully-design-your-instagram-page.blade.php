                    <h2 class="mt-4">Rules for designing a beautiful page for Instagram</h2>

                    <p><strong>Nickname.</strong> On Instagram, you can come up with a nickname only using Latin
                        letters. According to the rules, the profile name must be unique and contain no more than 30
                        characters. At the same time, subscribers should easily remember it.</p>
                    <p>If your blog is personal, please include your first name, last name, or nickname. If it's a
                        store or brand, then include the company name itself.</p>

                    <p>
