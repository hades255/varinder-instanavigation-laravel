                            in
                            the
                            form of photos and text.
                        </li>
                        <li>Creation of profiles not via a mobile device. At the moment there are many programs that
                            can
                            automatically register a large number of pages on a website. Instagram monitors and
                            prohibits
                            the use of such third-party apps. All accounts that were not created via a mobile device
                            are
                            permanently blocked.
                        </li>
                        <li>Publishing many posts with a break of a few seconds/minutes. Sometimes users create
                            pages
                            and
                            immediately start uploading a large number of photos, videos and posts there. Instagram
